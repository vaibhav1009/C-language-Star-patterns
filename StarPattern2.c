#include <stdio.h>

int main()
{
    int i, j, k ;
    
    for(i=7 ;i>=1 ;i--)
    {
        for(j=7 ;j>i ;j--)
        {
                printf(" ");
        }
        
        for(k=1; k<(i*2); k++)
        {
                printf("*");
        }
        printf("\n");
    } 
}
